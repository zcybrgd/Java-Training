package td2exo7;

public class Catalogue {
    Album[] albums;
    Magazine[] magazines;
    public void setAlbums(Album[] albums) {
        this.albums = albums;
    }
    public Album[] getAlbums() {
        return albums;
    }
    public Magazine[] getMagazines() {
        return magazines;
    }
    public void setMagazines(Magazine[] magazines) {
        this.magazines = magazines;
    }
}
